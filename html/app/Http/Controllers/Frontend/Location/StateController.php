    public function getStates($country_id){
        $states = State::select('id', 'name', 'country_id')
            ->where('country_id', '=', $country_id)
            ->where('enabled', '=', 1)
            ->where('removed', '=', 0)
            ->orderBy('name')
            ->get();
        return response_json('OK', 200, $states);
